/**
 * Integral
 */
public class Integral {

    public static void main(String[] args) {
        int decimalNumber = 102;    //decimal literal

        int binaryNumber = 0b1111; //binary literal

        int octalNumber = 0101;   //octal literal

        int hexa_DecimalNumber = 0X19Af; //hexaDecimal literal

        System.out.println(decimalNumber);
        System.out.println(binaryNumber);
        System.out.println(octalNumber);
        System.out.println(hexa_DecimalNumber);
    }
}