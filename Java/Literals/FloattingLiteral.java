public class FloattingLiteral {

    

    public static void main(String[] args) {
        
        float floatNumber = 1231.12f;
        
        double doubleNumber = 1.2423123423;
        
        System.out.println(floatNumber);
        System.out.println(doubleNumber);
    }
}